package Com.examprograms;

public class primenumbers {
	public static void main(String[] args) {
		
	
  int n=71;
  int m=0;
  for(int i=1;i<=n;i++) {
	 
		  if (n%i==0) {
			  m++;
			  
		  }
		  
	  }
	  if(m==2) {
		  System.out.println("prime");
	  
	  }else {
		   System.out.println("not prime");
	  }
	  
// table
	  
	  
	  int number = 5;
	  for(int i =1; i<=10;i++) {
		  System.out.println(number+ "*" +i+"="+number*i);
		  
	  }
		  
	
}
}

	  
	  
	  
  
	  
  